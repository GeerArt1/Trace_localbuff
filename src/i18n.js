// ── Current language state ──
window._lang = 'en';
var SUPPORTED_LANGS = ['en', 'de', 'fr', 'es', 'it'];
var LANG_LABELS = { en: 'English', de: 'Deutsch', fr: 'Français', es: 'Español', it: 'Italiano' };

/**
 * Translate a string key
 * @param {string} key - Dot-notation translation key
 * @param {Object} [vars] - Optional template variables ({count} etc)
 * @returns {string}
 */
window.__ = function __(key, vars) {
  var lang = window._lang || 'en';
  var dict = window.TRANSLATIONS[lang] || window.TRANSLATIONS.en;
  var enDict = window.TRANSLATIONS.en;
  var val = dict[key] || enDict[key] || key;
  if (vars) {
    for (let k in vars) {
      if (vars.hasOwnProperty(k)) {
        val = val.replace('{' + k + '}', vars[k]);
      }
    }
  }
  return val;
};

/**
 * Set the active language
 * @param {string} lang - Language code ('en', 'de', 'fr', 'es', 'it')
 */
window.setLanguage = function setLanguage(lang) {
  if (SUPPORTED_LANGS.indexOf(lang) < 0) lang = 'en';
  window._lang = lang;
  try { localStorage.setItem('trace_lang', lang); } catch(e) { TRACE_WATCHDOG?.warn('i18n', e); }
  document.documentElement.lang = lang;
  // First refresh tier UI (sets English from TIERS config), then translate DOM
  // to ensure data-i18n attributes override the tier defaults
  if (typeof window.setTier === 'function' && window.TIER) {
    window.setTier(window.TIER);
  }
  window.translateDOM();
  // Notify other modules
  if (typeof TRACE_REGISTRY !== 'undefined' && typeof TRACE_REGISTRY.emit === 'function') {
    TRACE_REGISTRY.emit('lang:changed', { lang: lang });
  }
};

/**
 * Translate all elements with data-i18n attributes in the DOM
 */
window.translateDOM = function translateDOM() {
  var els = document.querySelectorAll('[data-i18n]');
  for (let i = 0; i < els.length; i++) {
    var el = els[i];
    var key = el.getAttribute('data-i18n');
    if (!key) continue;
    var attr = el.getAttribute('data-i18n-attr');
    var text = window.__(key);
    if (attr) {
      el.setAttribute(attr, text);
    } else {
      el.textContent = text;
    }
  }
  // Update placeholder attributes
  var placeholders = document.querySelectorAll('[data-i18n-placeholder]');
  for (let j = 0; j < placeholders.length; j++) {
    var inp = placeholders[j];
    var pk = inp.getAttribute('data-i18n-placeholder');
    if (pk) inp.placeholder = window.__(pk);
  }
  // Update aria-labels
  var ariaEls = document.querySelectorAll('[data-i18n-aria]');
  for (let k = 0; k < ariaEls.length; k++) {
    var ael = ariaEls[k];
    var ak = ael.getAttribute('data-i18n-aria');
    if (ak) ael.setAttribute('aria-label', window.__(ak));
  }
};

/**
 * Get available languages
 * @returns {Array<{code:string,label:string}>}
 */
window.getLanguages = function getLanguages() {
  return SUPPORTED_LANGS.map(function(code) {
    return { code: code, label: LANG_LABELS[code] || code };
  });
};

/**
 * Initialize i18n — load saved language preference
 */
window.initI18n = function initI18n() {
  var saved = 'en';
  try { saved = localStorage.getItem('trace_lang') || 'en'; } catch(e) { TRACE_WATCHDOG?.warn('i18n', e); }
  window.setLanguage(saved);
};

// ── Language picker UI ──

/**
 * Toggle language picker visibility
 */
window.toggleLangPicker = function toggleLangPicker() {
  var picker = document.getElementById('lang-picker');
  if (!picker) return;
  var isVisible = picker.style.display !== 'none';
  picker.style.display = isVisible ? 'none' : 'block';
  if (!isVisible) {
    // Scroll to show the picker
    setTimeout(function() {
      picker.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }, 100);
  }
};

/**
 * Initialize language picker buttons in the profile screen
 */
window.initLangPicker = function initLangPicker() {
  var container = document.getElementById('lang-options');
  if (!container || container.hasChildNodes()) return;

  var currentLang = window._lang || 'en';
  var langs = window.getLanguages();

  langs.forEach(function(l) {
    var btn = document.createElement('button');
    btn.textContent = l.label;
    btn.dataset.lang = l.code;
    btn.style.cssText = 'padding:6px 12px;font-size:10px;letter-spacing:.08em;border:1px solid var(--border-mid);background:' +
      (l.code === currentLang ? 'var(--gold);color:#060402;' : 'var(--surface);color:var(--text-mid);') +
      ';cursor:pointer;transition:all .2s;border-radius:2px;';
    btn.onmouseenter = function() {
      if (this.dataset.lang !== window._lang) {
        this.style.borderColor = 'var(--gold)';
      }
    };
    btn.onmouseleave = function() {
      if (this.dataset.lang !== window._lang) {
        this.style.borderColor = 'var(--border-mid)';
      }
    };
    btn.onclick = function() {
      window.setLanguage(this.dataset.lang);
      // Update current display
      var currentEl = document.getElementById('lang-current');
      if (currentEl) currentEl.textContent = this.textContent;
      // Close picker
      var picker = document.getElementById('lang-picker');
      if (picker) picker.style.display = 'none';
      // Update button styles
      container.querySelectorAll('button').forEach(function(b) {
        if (b.dataset.lang === window._lang) {
          b.style.background = 'var(--gold)';
          b.style.color = '#060402';
          b.style.borderColor = 'var(--gold)';
        } else {
          b.style.background = 'var(--surface)';
          b.style.color = 'var(--text-mid)';
          b.style.borderColor = 'var(--border-mid)';
        }
      });
    };
    container.appendChild(btn);
  });
};

